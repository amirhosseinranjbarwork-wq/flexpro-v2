// Helper function to create food items
function createFood(
  name: string,
  category: string,
  subcategory: string | undefined,
  data: { u: string; b: number; c: number; p: number; ch: number; f: number },
  additional?: Partial<Food>
): Food {
  const id = name.toLowerCase()
    .replace(/\s+/g, '_')
    .replace(/[()]/g, '')
    .replace(/\//g, '_')
    .replace(/[،,]/g, '');
  
  // Determine meal timing based on category and macros
  let mealTiming: Food['mealTiming'] = ['anytime'];
  if (category.includes('پروتئین') && data.p > 20) {
    mealTiming = ['post-workout', 'anytime'];
  } else if (category.includes('کربوهیدرات') && data.ch > 20) {
    mealTiming = ['pre-workout', 'post-workout', 'anytime'];
  } else if (category.includes('چربی') && data.f > 10) {
    mealTiming = ['anytime', 'evening'];
  }

  // Determine macro ratio
  let macroRatio: Food['macroRatio'] = 'balanced';
  const totalMacros = data.p + data.ch + data.f;
  if (totalMacros > 0) {
    const pRatio = (data.p * 4) / data.c;
    const chRatio = (data.ch * 4) / data.c;
    const fRatio = (data.f * 9) / data.c;
    
    if (pRatio > 0.4) macroRatio = 'high-protein';
    else if (chRatio > 0.5) macroRatio = 'high-carb';
    else if (fRatio > 0.4) macroRatio = 'high-fat';
  }

  // Determine satiety index (1-10)
  let satietyIndex = 5;
  if (data.fiber && data.fiber > 5) satietyIndex += 2;
  if (data.p > 15) satietyIndex += 2;
  if (data.f > 10) satietyIndex += 1;
  satietyIndex = Math.min(10, Math.max(1, satietyIndex));

  // Determine digestibility
  let digestibility: Food['digestibility'] = 'medium';
  if (category.includes('پروتئین') && data.p > 20) {
    digestibility = name.includes('وی') ? 'fast' : 'slow';
  } else if (category.includes('کربوهیدرات') && data.fiber && data.fiber < 2) {
    digestibility = 'fast';
  } else if (data.fiber && data.fiber > 5) {
    digestibility = 'slow';
  }

  return {
    id,
    name,
    category,
    subcategory,
    unit: data.u as Food['unit'],
    baseAmount: data.b,
    calories: data.c,
    protein: data.p,
    carbs: data.ch,
    fat: data.f,
    isVegan: category.includes('گیاهی') || category.includes('میوه') || category.includes('سبزی') || category.includes('حبوبات'),
    isGlutenFree: !category.includes('غلات') && !category.includes('نان') && !name.includes('گندم'),
    allergens: category.includes('لبنیات') ? ['لبنیات'] : 
               category.includes('ماهی') || name.includes('ماهی') ? ['ماهی'] : 
               category.includes('تخم') || name.includes('تخم') ? ['تخم‌مرغ'] :
               name.includes('بادام') || name.includes('فندق') || name.includes('پسته') ? ['آجیل'] : [],
    mealTiming,
    macroRatio,
    satietyIndex,
    digestibility,
    costLevel: category.includes('فست‌فود') ? 'high' : 
               category.includes('آجیل') ? 'high' : 
               category.includes('ماهی') && name.includes('سالمون') ? 'high' : 'medium',
    ...additional
  };
}

