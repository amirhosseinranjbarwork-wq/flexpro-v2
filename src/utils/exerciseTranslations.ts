/**
 * Translate exercise name to Persian with English in parentheses
 */
export function translateExerciseName(englishName: string): string {
  return exerciseTranslations[englishName] || `${englishName} (${englishName})`;
}

/**
 * Get Persian name only (without English)
 */
export function getPersianName(englishName: string): string {
  const translated = translateExerciseName(englishName);
  const match = translated.match(/^(.+?)\s*\(/);
  return match ? match[1].trim() : englishName;
}


